public class ProxyPreviewEntity {

    private int totalProxy;
    private int second;
    private int highestProxy;
    private int sysTotalNum;



    public int getTotalProxy() {
        return totalProxy;
    }

    public void setTotalProxy(int totalProxy) {
        this.totalProxy = totalProxy;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public int getHighestProxy() {
        return highestProxy;
    }

    public void setHighestProxy(int highestProxy) {
        this.highestProxy = highestProxy;
    }

    public int getSysTotalNum() {
        return sysTotalNum;
    }

    public void setSysTotalNum(int sysTotalNum) {
        this.sysTotalNum = sysTotalNum;
    }
}
